declare(strict_types=1);

namespace Tigren\Pwa\Model\Resolver\Product;

use Magento\Framework\GraphQl\Config\Element\Field;
use Magento\Framework\GraphQl\Exception\GraphQlInputException;
use Magento\Framework\GraphQl\Query\ResolverInterface;
use Magento\Framework\GraphQl\Schema\Type\ResolveInfo;

/**
 * Class BasePriceExclTax
 * @package Tigren\Pwa\Model\Resolver\Product
 */
class BasePriceExclTax implements ResolverInterface
{
    /**
     * @param Field $field
     * @param $context
     * @param ResolveInfo $info
     * @param array|null $value
     * @param array|null $args
     * @return \Magento\Framework\GraphQl\Query\Resolver\Value|mixed
     * @throws GraphQlInputException
     */
    public function resolve(
        Field $field,
        $context,
        ResolveInfo $info,
        array $value = null,
        array $args = null
    ) {
        $product = $value['model'];

        if (empty($product)) {
            throw new GraphQlInputException(__('Must be has model product'));
        }

        return (float)$product->getPriceInfo()->getPrice('base_price')->getAmount()->getBaseAmount();
    }
}
